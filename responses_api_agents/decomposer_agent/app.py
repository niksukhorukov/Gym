class DecomposerAgent(SimpleResponsesAPIAgent):
    """Gym agent server for the first Decomposer version.

    Assumes LangGraph SDK subagent assistants are already running. For now,
    subagents do not receive Gym resource-server tool/session context.
    """

    model_config = ConfigDict(extra="allow", arbitrary_types_allowed=True)
    config: DecomposerAgentConfig

    def __init__(self, *args, **kwargs):
        super().__init__(*args, **kwargs)

        self.graph = create_decomposer_agent(
            decomposer_model=ChatNeMoGym(
                server_client=self.server_client,
                model_server_name=self.config.model_server.name,
            ),
            subagent_types=self.config.subagent_types,
            middleware=[NeMoGymMiddleware()],
            context_schema=NeMoGymContext,
        )

    async def responses(
        self,
        request: Request,
        response: Response,
        body: NeMoGymResponseCreateParamsNonStreaming = Body(),
    ) -> NeMoGymResponse:
        body = body.model_copy(deep=True)

        input_messages = _input_to_messages(body.input)
        initial_state = {"messages": input_messages}
        final_state = await self.graph.ainvoke(
            initial_state,
            context=NeMoGymContext(body=body),
        )
        all_messages = final_state["messages"]
        output_messages = all_messages[len(input_messages) :]
        output_items = _messages_to_items(output_messages)
        usage = _messages_to_usage(output_messages)

        model_response = None
        for message in reversed(output_messages):
            if isinstance(message, AIMessage) and "nemo_gym_response" in message.response_metadata:
                model_response = message.response_metadata["nemo_gym_response"]
                break
        if model_response is None:
            raise RuntimeError("Decomposer graph did not produce a `ChatNeMoGym` model response.")

        for k, v in request.cookies.items():
            response.set_cookie(k, v)
        model_server_cookies = _latest_cookies(output_messages)
        if model_server_cookies is not None:
            for k, v in model_server_cookies.items():
                response.set_cookie(k, v)

        return NeMoGymResponse.model_validate(model_response | {"output": output_items, "usage": usage})

    async def run(
        self,
        request: Request,
        body: DecomposerAgentRunRequest = Body(),
    ) -> DecomposerAgentVerifyResponse:
        cookies = request.cookies

        seed_session_response = await self.server_client.post(
            server_name=self.config.resources_server.name,
            url_path="/seed_session",
            json=body.model_dump(),
            cookies=cookies,
        )
        await raise_for_status(seed_session_response)
        cookies = seed_session_response.cookies

        response = await self.server_client.post(
            server_name=self.config.name,
            url_path="/v1/responses",
            json=body.responses_create_params,
            cookies=cookies,
        )
        await raise_for_status(response)
        cookies = response.cookies

        verify_request = DecomposerAgentVerifyRequest.model_validate(
            body.model_dump() | {"response": await get_response_json(response)}
        )

        verify_response = await self.server_client.post(
            server_name=self.config.resources_server.name,
            url_path="/verify",
            json=verify_request.model_dump(),
            cookies=cookies,
        )
        await raise_for_status(verify_response)
        return DecomposerAgentVerifyResponse.model_validate(await get_response_json(verify_response))

    async def aggregate_metrics(self, body: AggregateMetricsRequest = Body()) -> AggregateMetrics:
        response = await self.server_client.post(
            server_name=self.config.resources_server.name,
            url_path="/aggregate_metrics",
            json=body.model_dump(),
        )
        await raise_for_status(response)
        return AggregateMetrics.model_validate(await get_response_json(response))


def _input_to_messages(input: str | NeMoGymResponseInput) -> list[BaseMessage]:
    """Convert Gym Responses API `input` into LangChain `messages`."""
    if isinstance(input, str):
        return [HumanMessage(content=input)]

    messages = []
    for item in input:
        item_type = _item_get(item, "type")
        if item_type == "message":
            role = _item_get(item, "role")
            content = _input_content_to_text(_item_get(item, "content", ""))

            if role == "user":
                message = HumanMessage(content=content)
            elif role in {"system", "developer"}:
                message = SystemMessage(content=content)
            elif role == "assistant":
                message = AIMessage(content=content)
            else:
                raise NotImplementedError(f"Unsupported Gym message `role`: {role!r}")

        elif item_type == "function_call":
            arguments = _item_get(item, "arguments", "")
            call_id = _item_get(item, "call_id")
            name = _item_get(item, "name")
            try:
                args = json.loads(arguments)
            except json.JSONDecodeError as e:
                message = AIMessage(
                    content="",
                    invalid_tool_calls=[
                        {
                            "type": "invalid_tool_call",
                            "id": call_id,
                            "name": name,
                            "args": arguments,
                            "error": f"Invalid JSON arguments: {e}",
                        }
                    ],
                )
            else:
                if not isinstance(args, dict):
                    message = AIMessage(
                        content="",
                        invalid_tool_calls=[
                            {
                                "type": "invalid_tool_call",
                                "id": call_id,
                                "name": name,
                                "args": arguments,
                                "error": "Tool-call arguments must be a JSON object.",
                            }
                        ],
                    )
                else:
                    message = AIMessage(
                        content="",
                        tool_calls=[{"name": name, "args": args, "id": call_id}],
                    )
        elif item_type == "function_call_output":
            message = ToolMessage(
                content=_input_content_to_text(_item_get(item, "output", "")),
                tool_call_id=_item_get(item, "call_id"),
            )
        else:
            if item_type in _MULTIMODAL_CONTENT_TYPES:
                raise NotImplementedError(
                    "`DecomposerAgent` supports text-only input; "
                    f"multimodal input item type {item_type!r} is not supported."
                )
            raise NotImplementedError(f"Unsupported Gym input item `type`: {item_type!r}")

        messages.append(message)

    return messages


def _request_with_body(request: ModelRequest) -> ModelRequest:
    body = _item_get(request.runtime.context, "body")
    if body is None:
        raise RuntimeError("`NeMoGymMiddleware` expected runtime `context.body`.")

    model_settings = dict(request.model_settings)
    model_settings["nemo_gym_body"] = NeMoGymResponseCreateParamsNonStreaming.model_validate(body)
    return request.override(model_settings=model_settings)


def _messages_to_items(messages: list[BaseMessage]) -> list[NeMoGymResponseInputItem | NeMoGymResponseOutputItem]:
    """Convert LangChain `messages` into Gym Responses API input/output items."""
    items = []
    for message in messages:
        if isinstance(message, HumanMessage):
            items.append(
                NeMoGymEasyInputMessage(role="user", content=_message_content_to_text(message.content)).model_dump()
            )
        elif isinstance(message, SystemMessage):
            items.append(
                NeMoGymEasyInputMessage(role="system", content=_message_content_to_text(message.content)).model_dump()
            )
        elif isinstance(message, ToolMessage):
            items.append(
                NeMoGymFunctionCallOutput(
                    call_id=message.tool_call_id,
                    output=_message_content_to_text(message.content),
                    status="completed",
                ).model_dump(exclude_none=True)
            )
        elif isinstance(message, AIMessage):
            if "nemo_gym_response" in message.response_metadata:
                # Preferred path for runtime messages generated by `ChatNeMoGym`:
                # replay the exact Gym model-server output, including training fields.
                output_items = message.response_metadata["nemo_gym_response"]["output"]
            else:
                # Fallback for hand-authored LangChain assistant messages, especially
                # future few-shot examples. These are prompt items only; they should not
                # be treated as generated rollout output by `DecomposerAgent.responses()`.
                output_items = []
                if message.content:
                    output_items.append(
                        NeMoGymEasyInputMessage(
                            role="assistant",
                            content=_message_content_to_text(message.content),
                        ).model_dump()
                    )
                for tool_call in message.tool_calls:
                    output_items.append(
                        NeMoGymResponseFunctionToolCall(
                            name=tool_call["name"],
                            arguments=json.dumps(tool_call["args"]),
                            call_id=tool_call["id"],
                            status="completed",
                        ).model_dump(exclude_none=True)
                    )
            items.extend(output_items)
        else:
            raise NotImplementedError(f"Unsupported LangChain message `type`: {type(message).__name__}")

    return items


def _messages_to_usage(messages: list[BaseMessage]) -> NeMoGymResponseUsage | None:
    usage = None
    for message in messages:
        if not isinstance(message, AIMessage):
            continue

        response = message.response_metadata.get("nemo_gym_response")
        if isinstance(response, NeMoGymResponse):
            response_usage = response.usage
        elif isinstance(response, dict) and response.get("usage") is not None:
            response_usage = NeMoGymResponseUsage.model_validate(response["usage"])
        else:
            response_usage = None

        if response_usage is None:
            continue

        if usage is None:
            usage = response_usage
        else:
            usage.input_tokens += response_usage.input_tokens
            usage.output_tokens += response_usage.output_tokens
            usage.total_tokens += response_usage.total_tokens

            # Match Gym examples: keep aggregate nested details conservative.
            usage.input_tokens_details.cached_tokens = 0
            usage.output_tokens_details.reasoning_tokens = 0

    return usage


def _response_to_ai_message(response: NeMoGymResponse) -> AIMessage:
    """Project one Gym response into the `AIMessage` shape `create_agent` expects."""
    texts: list[str] = []
    tool_calls: list[dict[str, Any]] = []
    invalid_tool_calls: list[dict[str, Any]] = []

    for item in response.output:
        if item.type == "message":
            for content in item.content:
                if content.type == "output_text":
                    texts.append(content.text)
                elif content.type == "refusal":
                    texts.append(content.refusal)
        elif item.type == "function_call":
            try:
                args = json.loads(item.arguments)
            except json.JSONDecodeError as e:
                # Invalid tool calls are represented with LangChain's standard
                # field. `create_agent` will not execute them; repair/retry policy
                # belongs in an optional middleware, not this model wrapper.
                invalid_tool_calls.append(
                    {
                        "type": "invalid_tool_call",
                        "id": item.call_id,
                        "name": item.name,
                        "args": item.arguments,
                        "error": f"Invalid JSON arguments: {e}",
                    }
                )
                continue

            if not isinstance(args, dict):
                # LangChain executable tool calls require dict args.
                invalid_tool_calls.append(
                    {
                        "type": "invalid_tool_call",
                        "id": item.call_id,
                        "name": item.name,
                        "args": item.arguments,
                        "error": "Tool-call arguments must be a JSON object.",
                    }
                )
                continue

            tool_calls.append({"name": item.name, "args": args, "id": item.call_id})

    usage_metadata = None
    if response.usage is not None:
        usage_metadata = {
            "input_tokens": response.usage.input_tokens,
            "output_tokens": response.usage.output_tokens,
            "total_tokens": response.usage.total_tokens,
        }

    return AIMessage(
        content="\n".join(text for text in texts if text),
        tool_calls=tool_calls,
        invalid_tool_calls=invalid_tool_calls,
        usage_metadata=usage_metadata,
    )


def _latest_cookies(messages: list[BaseMessage]) -> Any | None:
    """Return the most recent model-server cookies stored on an AI message."""
    for message in reversed(messages):
        if not isinstance(message, AIMessage):
            continue
        if "nemo_gym_cookies" in message.response_metadata:
            return message.response_metadata["nemo_gym_cookies"]
    return None


def _to_responses_tool(tool: dict[str, Any] | type | Callable[..., Any] | BaseTool) -> dict[str, Any]:
    openai_tool = tool if isinstance(tool, dict) else convert_to_openai_tool(tool)

    if openai_tool.get("type") == "function" and "function" in openai_tool:
        responses_tool = {"type": "function", **openai_tool["function"]}
    elif openai_tool.get("type") == "function":
        responses_tool = dict(openai_tool)
    else:
        return openai_tool

    responses_tool.setdefault("strict", False)
    return responses_tool


def _to_responses_tool_choice(tool_choice: Any) -> Any:
    if isinstance(tool_choice, str):
        return tool_choice
    if isinstance(tool_choice, dict) and tool_choice.get("type") == "function" and "function" in tool_choice:
        return {"type": "function", "name": tool_choice["function"]["name"]}
    return tool_choice


def _message_content_to_text(content: Any) -> str:
    return _content_to_text(content, source="LangChain")


def _input_content_to_text(content: Any) -> str:
    return _content_to_text(content, source="Gym")


_TEXT_CONTENT_TYPES = frozenset({"text", "input_text", "output_text"})
_MULTIMODAL_CONTENT_TYPES = frozenset(
    {
        "audio",
        "computer_call_output",
        "file",
        "image",
        "image_generation_call",
        "image_url",
        "input_audio",
        "input_file",
        "input_image",
        "video",
    }
)


def _content_to_text(content: Any, *, source: str) -> str:
    if isinstance(content, str):
        return content
    if not isinstance(content, list):
        raise NotImplementedError(
            f"`DecomposerAgent` supports only string or text-part {source} message content; "
            f"received {type(content).__name__}."
        )

    texts = []
    for part in content:
        if isinstance(part, str):
            texts.append(part)
            continue

        part_type = _item_get(part, "type")
        if part_type in _MULTIMODAL_CONTENT_TYPES:
            raise NotImplementedError(
                f"`DecomposerAgent` supports text-only input; multimodal {source} "
                f"content part type {part_type!r} is not supported."
            )
        if part_type in _TEXT_CONTENT_TYPES:
            text = _item_get(part, "text")
        elif part_type == "refusal":
            text = _item_get(part, "refusal")
        else:
            raise NotImplementedError(f"Unsupported {source} message content part `type`: {part_type!r}")

        if not isinstance(text, str):
            raise ValueError(f"{source} message content part {part_type!r} must contain string text.")
        texts.append(text)

    return "\n".join(texts)


def _item_get(item: Any, key: str, default: Any = None) -> Any:
    if isinstance(item, dict):
        return item.get(key, default)
    return getattr(item, key, default)


