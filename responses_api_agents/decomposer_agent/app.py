class ChatNeMoGym(BaseChatModel):
    """Gym model-server wrapper that can be used as `decomposer_model` in
    `create_decomposer_agent`.

    How it works:
    - `create_decomposer_agent` passes this model to LangChain's `create_agent`.
    - `create_agent` owns the agent loop. On every model step, it calls
      `bind_tools()` with `spawn_subagent` and `wait` tools and then calls
      `ainvoke()`.
    - `bind_tools()` converts LangChain/OpenAI tool schemas to Gym Responses API
      function-tool schemas and stores them as bound kwargs.
    - `BaseChatModel.ainvoke()` eventually calls `_agenerate()` with the current
      LangChain messages plus bound kwargs such as `tools` and `tool_choice`.
    - `_agenerate()` starts from the original Gym request body, replaces only
      `input` with the current LangChain `messages`, applies bound `tools` /
      `tool_choice` kwargs,
      calls the configured Gym model server `/v1/responses`, and converts the
      Gym `NeMoGymResponse` back to a LangChain `AIMessage`.
    - Runtime AI messages generated by this wrapper carry the raw Gym response
      in `ChatResult.llm_output["nemo_gym_response"]`; LangChain copies that
      into `AIMessage.response_metadata`. `ainvoke()` asserts this contract.
    - On later model calls, output AI messages are replayed from the exact
      stored Gym response output items. This preserves Gym-native trajectory and
      training fields instead of reconstructing them from LangChain messages.
    - Input AI messages (e.g. few-shot examples) might not have
      `nemo_gym_response`; they are converted from normal LangChain `content`
      and `tool_calls`.
    - Model-server cookies are stored in
      `ChatResult.llm_output["nemo_gym_cookies"]` and replayed to the next model
      request, matching Gym official examples.
    - Malformed function-call arguments become `AIMessage.invalid_tool_calls`.
      `create_agent` will not execute them; any repair/retry policy should be
      implemented outside this model wrapper.

    Note that we implement only `ainvoke` and `_agenerate`, because in the
    `DecomposerAgent` below only `ainvoke` is used.
    """

    model_config = ConfigDict(extra="allow", arbitrary_types_allowed=True)

    server_client: Any
    model_server_name: str

    @property
    def _llm_type(self) -> str:
        return "nemo_gym"

    async def ainvoke(
        self,
        input: Any,
        config: Any | None = None,
        *,
        stop: list[str] | None = None,
        **kwargs: Any,
    ) -> AIMessage:
        """Run the normal `BaseChatModel` path and assert LangChain metadata behavior."""
        message = await super().ainvoke(input, config=config, stop=stop, **kwargs)
        missing_metadata = {"nemo_gym_response", "nemo_gym_cookies"} - set(message.response_metadata)
        if missing_metadata:
            raise RuntimeError(
                "Expected LangChain to copy `ChatResult.llm_output` into "
                "`AIMessage.response_metadata`, but missing keys: "
                f"{sorted(missing_metadata)}"
            )
        return message

    def _generate(
        self,
        messages: list[BaseMessage],
        stop: list[str] | None = None,
        run_manager: CallbackManagerForLLMRun | None = None,
        **kwargs: Any,
    ) -> ChatResult:
        raise NotImplementedError("`ChatNeMoGym` supports async generation only.")

    async def _agenerate(
        self,
        messages: list[BaseMessage],
        stop: list[str] | None = None,
        run_manager: AsyncCallbackManagerForLLMRun | None = None,
        **kwargs: Any,
    ) -> ChatResult:
        """Call the configured Gym model server once.

        The request body starts from the original Gym `/v1/responses` body passed
        through runtime `context` by `NeMoGymMiddleware`. We replace only
        `input` for the current model step and then apply kwargs bound by
        `create_agent`, mainly `tools` and `tool_choice`.
        """
        if stop:
            raise NotImplementedError("`ChatNeMoGym` does not support `stop` sequences yet.")

        body = kwargs.pop("nemo_gym_body", None)
        if body is None:
            raise RuntimeError("`ChatNeMoGym` expected `NeMoGymMiddleware` to pass `nemo_gym_body`.")
        body = NeMoGymResponseCreateParamsNonStreaming.model_validate(body).model_dump(exclude_unset=True)
        body["input"] = _messages_to_items(messages)

        for key in ("max_output_tokens", "temperature", "top_p", "top_logprobs", "parallel_tool_calls"):
            if kwargs.get(key) is not None:
                body[key] = kwargs[key]
        if kwargs.get("max_tokens") is not None and kwargs.get("max_output_tokens") is None:
            body["max_output_tokens"] = kwargs["max_tokens"]

        tools = kwargs.get("tools")
        if not tools:
            raise RuntimeError("`ChatNeMoGym` expected `create_agent` to bind non-empty Decomposer `tools`.")
        body["tools"] = [_to_responses_tool(tool) for tool in tools]

        tool_choice = kwargs.get("tool_choice")
        body["tool_choice"] = _to_responses_tool_choice(tool_choice) if tool_choice is not None else "auto"

        body = NeMoGymResponseCreateParamsNonStreaming.model_validate(body)

        model_response = await self.server_client.post(
            server_name=self.model_server_name,
            url_path="/v1/responses",
            json=body,
            cookies=_latest_cookies(messages),
        )
        await raise_for_status(model_response)
        response_json = await get_response_json(model_response)
        response = NeMoGymResponse.model_validate(response_json)

        message = _response_to_ai_message(response)
        return ChatResult(
            generations=[ChatGeneration(message=message)],
            llm_output={
                # LangChain copies `llm_output` into `AIMessage.response_metadata` for
                # the single-generation case. `DecomposerAgent.responses()` can then
                # recover Gym-native outputs without custom graph middleware.
                "nemo_gym_response": response_json,
                # Keep Gym/aiohttp cookies as-is, matching official Gym examples.
                # The next model call scans prior AI messages and passes them back.
                "nemo_gym_cookies": model_response.cookies,
            },
        )

    def bind_tools(
        self,
        tools: Sequence[dict[str, Any] | type | Callable[..., Any] | BaseTool],
        *,
        tool_choice: str | None = None,
        **kwargs: Any,
    ):
        """Bind Decomposer tools in the format expected by Gym Responses API.

        `create_agent` calls this before every model invocation when `tools` are
        available. `self.bind(...)` stores these kwargs until `BaseChatModel.ainvoke()`
        eventually forwards them to `_agenerate()`.
        """
        bound_kwargs = {"tools": [_to_responses_tool(tool) for tool in tools]}
        if tool_choice is not None:
            bound_kwargs["tool_choice"] = _to_responses_tool_choice(tool_choice)
        bound_kwargs.update(kwargs)
        return self.bind(**bound_kwargs)


@dataclass(frozen=True)
class NeMoGymContext:
    body: NeMoGymResponseCreateParamsNonStreaming


class NeMoGymMiddleware(AgentMiddleware):
    def wrap_model_call(
        self,
        request: ModelRequest,
        handler: Callable[[ModelRequest], ModelResponse],
    ) -> ModelResponse:
        return handler(_request_with_body(request))

    async def awrap_model_call(
        self,
        request: ModelRequest,
        handler: Callable[[ModelRequest], Awaitable[ModelResponse]],
    ) -> ModelResponse:
        return await handler(_request_with_body(request))


